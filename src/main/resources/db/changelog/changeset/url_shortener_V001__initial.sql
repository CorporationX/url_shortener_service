CREATE TABLE url
(
    id         BIGINT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    hash       VARCHAR(6) NOT NULL UNIQUE,
    url        TEXT       NOT NULL,
    created_at TIMESTAMP  NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE hash
(
    id   BIGINT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    hash VARCHAR(6) NOT NULL UNIQUE
);

CREATE SEQUENCE unique_hash_number_seq
    START WITH 1
    INCREMENT BY 1;

